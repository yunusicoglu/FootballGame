package galatasaray;

public class Galatasaray {
	String name;
	int pass;
	int shot;
	int dribbling;
	int passAfterDribbling;
	int shotAferDribbling;
	
	public String getName() {
		return name;
	}
	public int getPass() {
		return pass;
	}
	public int getShot() {
		return shot;
	}
	public int getDribbling() {
		return dribbling;
	}
	public int getPassAfterDribbling() {
		return passAfterDribbling;
	}
	public int getShotAferDribbling() {
		return shotAferDribbling;
	}
}
